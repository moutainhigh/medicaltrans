package com.segi.uhomecp.wh.common.enums;

public enum DataSourceEnum {
	
	DATA_SOURCE_IOS("2", "苹果手机"),
	
	DATA_SOURCE_PAD_IOS("3", "苹果平板"),
	
	DATA_SOURCE_ANDROID("4", "安卓手机"),
	
	DATA_SOURCE_PAD_ANDROID("5", "安卓平板"),
	
	DATA_SOURCE_WEB("10", "Web"),
	
	DATA_SOURCE_WECHAT("11", "公众号");
	
	private String code;
		
	private String name;
	
	private DataSourceEnum(String code, String name) {
		this.code = code;
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public String getName() {
		return name;
	}
}
